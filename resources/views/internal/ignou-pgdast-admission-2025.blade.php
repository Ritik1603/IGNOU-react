            function bindTilt(el, opts) {
                const strength = opts?.strength ?? 12;
                const damp = opts?.damp ?? 8;
                let raf = null, rx = 0, ry = 0;
                function onMove(e) {
                    const rect = el.getBoundingClientRect();
                    const cx = (e.clientX - rect.left) / rect.width;
                    const cy = (e.clientY - rect.top) / rect.height;
                    const tRY = (cx - 0.5) * strength * 2;
                    const tRX = (0.5 - cy) * strength * 2;
                    function step() {
                        rx += (tRX - rx) / damp;
                        ry += (tRY - ry) / damp;
                        el.style.setProperty('--rx', rx.toFixed(2) + 'deg');
                        el.style.setProperty('--ry', ry.toFixed(2) + 'deg');
                        el.style.setProperty('--mx', clamp(cx * 100, 0, 100) + '%');
                        el.style.setProperty('--my', clamp(cy * 100, 0, 100) + '%');
                        if (Math.abs(tRX - rx) > 0.1 || Math.abs(tRY - ry) > 0.1) raf = requestAnimationFrame(step);
                        else { cancelAnimationFrame(raf); raf = null; }
                    }
                    if (!raf) raf = requestAnimationFrame(step);
                }
                function onLeave() { el.style.setProperty('--rx', '0deg'); el.style.setProperty('--ry', '0deg'); el.style.removeProperty('--mx'); el.style.removeProperty('--my'); }
