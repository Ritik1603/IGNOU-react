        function setActive() {
            const y = $(window).scrollTop() + 120;
            const ids = ['#home', '#why', '#highlights', '#structure', '#syllabus', '#eligibility', '#careers', '#faq', '#contact'];
            let current = '#home';
            ids.forEach(id => {
                const $el = $(id);
                if (!$el.length) return;
                const top = $el.offset().top,
                    h = $el.outerHeight();
                if (y >= top && y < top + h) current = id;
            });
            const buttonTarget = current === '#why' ? '#home' : (current === '#structure' || current === '#syllabus' ? '#syllabus' : (current === '#eligibility' || current === '#careers' || current === '#faq' ? '#careers' : current));
            $('#dock button').removeClass('active');
            $(`#dock button[data-target="${buttonTarget}"]`).addClass('active');
            updateNavIcons();
        }

