                <h3>Curriculum Structure and Academic Design</h3>
                <p>The IGNOU BA Economics follows the Choice-Based Credit System. This ensures a balanced mixture of theoretical, analytic, and skills-oriented learning. Over six semesters, students will earn 148 credits, which are divided into five categories. Core Courses; Discipline-Specific electives; Ability Enhancement courses; Skill Enhancement courses, and Generic Electives.</p>
                <p>Each credit is equal to 30 hours of work, including self learning, assignments, counseling sessions, and project work.</p>
                <p>The course progresses from the foundational topics of micro-and macroeconomics up to advanced subjects such as econometrics. Quantitative techniques, policy analysis, and development theory.</p>
                <p>Instead of memorizing, we focus on conceptual clarity, data analysis, and policy implementation, which will help you to understand how economics works in the real world.</p>
                <p>The program introduces modules on communication, environment, and research skills, which help students connect economics with society and sustainability.</p>
