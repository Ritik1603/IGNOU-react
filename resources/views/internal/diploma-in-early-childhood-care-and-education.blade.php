                <h2>What is the Program?</h2>
                <p>The Diploma in Early Childhood Care and Education, or DECE, is an integrated programme that explains children's growth in terms of their physical, mental, and social development. Anyone who works in the field of education knows how rapidly these changes take place and how influential this period is on a child's personality.</p>
                <p>You will learn the following:</p>
