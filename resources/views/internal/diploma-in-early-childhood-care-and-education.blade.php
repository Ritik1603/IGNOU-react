                    <li>How to plan meaningful and safe activities</li>
                    <li>How to help children with their emotional needs</li>
                    <li>Health and nutrition affect growth.</li>
                    <li>What to say to parents</li>
                    <li>How to manage early childhood environments that are diverse</li>
                </ul>
                <p>The program is designed for children between the ages of six and twelve, including infants, toddlers and pre-primary students. To work with children of this age group, you need patience, understanding, and proper training. Children imitate, react and express themselves differently than adults. This course helps you to interpret these behaviors correctly.</p>
                <p>The program is special because it recognises that care for young children is not just about teaching, but also about nurturing. It includes routines and conversations, play-based learning, safety for children, hygiene, nutrition, and responsive caring. This diploma will teach you the connections between all of these areas.</p>
